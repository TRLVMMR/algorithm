/*
	求斐波那契数列余10007的余数,直接求求出数据会太大（除非用大数）
	
	思路：把数列变成以斐波那契为基础的，10007的余数数列 
	
	处理方法：一但某项大于10007，就减掉10007 
*/
#include<stdio.h>

int f[1000000];

int main()
{
	f[0] = f[1] = 1;
	
	int n;
	
	scanf("%d", &n);
	
	for(int i = 2; i < n; i++)
	{
		f[i] = f[i-1] + f[i-2];
		while(f[i] >= 10007)
		{
			f[i] -= 10007;
		}
	}
	
	printf("%d", f[n-1]);
	
	return 0;
}
